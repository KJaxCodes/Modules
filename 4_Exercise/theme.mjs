export function setLightTheme() {
    console.log("Light theme applied.");
    document.body.style.backgroundColor = "#f0f6f7"; //light blue background
    document.body.style.color = "#000000"; //black font
}

export function setDarkTheme() {
    console.log("Dark theme applied.");
    document.body.style.backgroundColor = "#1e1e1e"; //dark gray background
    document.body.style.color = "#ffffff"; //white font
}